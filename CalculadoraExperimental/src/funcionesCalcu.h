/** \brief Sumar dos numeros
 *
 * \param num1
 * \param num2
 * \return num1+num2
 *
 */

float suma(float num1, float num2);

/** \brief Restar dos numeros
 *
 * \param num1

 * \param num2
 * \return num1-num2
 *
 */

float resta(float num1, float num2);

/** \brief multiplicar dos numeros
 *
 * \param num1
 * \param num2
 * \return num1*num2
 *
 */

float multiplicacion(float num1, float num2);

/** \brief dividir dos numeros
 *
 * \param num1
 * \param num2
 * \return num1/num2
 *
 */

float division(float num1, float num2);

/** \brief factorial de un numero natural positivo
 *
 * \param num1

 * \return num1! = num1 * (num1-1) * (num1-2) * ... * 1
 *
 */

int factorial(float num1);
